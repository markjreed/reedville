  <h2 class="headlne">Houston County Commission decision about
  rezoning at second meeting could have been illegal.</h2>

  <p>11 September 2003</p>

  <p><font size="3">Editor:<br />
  <br />
  On August 5<sup>th</sup>, I attended a meeting of the Houston
  County Commission to speak against two re-zoning petitions for
  two tracts along Sandefur Road and Lake Joy Road.<br />
  <br />
  The first request was to re-zone 59.31 acres from R-1 to R2. The
  second request was to re-zone 32.21 acres from R-1 to R-3. The
  first request was approved by the Board of Commissioners.<br />
  <br />
  I do not oppose development of the property in question. Rather,
  I am opposed to the re-zoning requests because of the density of
  housing that the requested new zoning would permit. In fact, the
  maps presented by the developer at that meeting revealed that 295
  homes were to be built on the 91.52 acres. Therefore, my
  opposition arises because it is in the interests of all current
  and future residents of Houston County that future development
  takes into consideration the negative aspects of high density
  development. The elements that arise as a result of this request
  that create my concern and my opposition to the current requests
  are:<br />
  <br /></font></p>

  <ol>
    <li type="1"><font size="3">This request has been made noting
    the need for entry level housing in Houston County as a means
    to prevent the closing of Robins AFB as a result of any future
    BRAC actions. This is nonsense! There are entry level
    subdivisions currently being developed and proposed to be
    developed throughout the northern half of Houston County. Their
    locations are along highway 96, Lakeview Road, Feagin Mill
    Road, Houston Lake Road, and many others. There simply is not a
    need for more &ldquo;entry level&rdquo; housing. This is
    especially true when there is an abundance of existing housing
    that offers much more square footage for a lower price than new
    construction &ldquo;entry level&rdquo;
    housing.<br /></font></li>
  </ol><font size="3"><br /></font>

  <ol>
    <li type="1"><font size="3">There is already an overwhelming
    problem with drainage as a result of the ever increasing
    development that has occurred in the immediate past.
    Apparently, there is no program within the county to adequately
    address the problem. As an example, on the late afternoon of
    July 18, a shower of short duration created a flooding of
    properties in the vicinity of the intersection of highway 96
    and Houston Lake Road. As a result, several parking lots had to
    be closed.<br /></font></li>
  </ol><font size="3"><br />
  Another example of the problems created by flooding created by
  and exacerbated by rapid development can be cited by reference to
  the problems encountered by residents of Peachtree Estates.
  Whenever they sought resolution for their problems, they found
  themselves getting the old run around. It isn&rsquo;t the
  county&rsquo;s problem. Developers stated that is wasn&rsquo;t
  their problem to fix, it was the county&rsquo;s problem to
  correct. In the end, residents are faced with no support to
  resolve their problems.<br />
  <br /></font>

  <ol>
    <li type="1"><font size="3">In reference to Lake Joy Road,
    recent development and already approved re-zoning applications
    will add another 1000 homes along either side of the road from
    the intersection of highway 96 and Lake Joy Road (83 single
    family units and 300 apartment units) to the south end just
    north of highway 127 (already approved for multi-family
    housing). Langston Road has another subdivision with nearly 200
    houses to be built in the near future. Much of the traffic from
    there will travel north along Lake Joy to reach the more
    industrialized areas of Warner Robins and Robins AFB.
    Mallard&rsquo;s Landing is still being completed. Expansion of
    Cardinal Ridge and Royal Oaks at their intersections to
    Sandefur Road will add more traffic to Lake
    Joy.<br /></font></li>
  </ol><font size="3"><br />
  At the moment, I am unaware of any plans to expand Lake Joy Road
  in the near future. That leaves the existing road, with its
  limited capacity, to handle several thousand more vehicle
  transits each day. Already, accidents are on the increase. This
  is simply not an acceptable situation.<br />
  <br /></font>

  <ol>
    <li type="1"><font size="3">Continued development of high
    density subdivisions will create huge demands upon an already
    stressed to capacity school system. We build new schools and
    place trailers at the front door upon their opening. We have a
    shortage of teachers, with the demands of the federal &ldquo;No
    Child Left Behind&rdquo; program. The state has cut its funding
    for education, due to its budgetary shortfalls. That leaves
    Houston County residents to be confronted with one SPLOST after
    another. Additionally, there can be little doubt that
    increasing millage rates by the Board of Education to meet the
    ever increasing expenses will have to be given serious
    consideration. These tax burdens fall heavily upon an already
    overtaxed populace. And, it should be noted a large percentage
    of our residents are retirees, living on a fixed
    income<br /></font></li>
  </ol><font size="3"><br /></font>

  <ol>
    <li type="1"><font size="3">The air quality of the Macon
    metropolitan area is already among the worst in the nation. At
    the moment, I believe that Houston County is not under federal
    or state mandate to improve its air quality. But, we are in
    certain danger of falling under those guidelines as long as we
    continue the unabated development. That would cost our
    taxpayers millions of dollars to correct. If it is ever
    corrected to the satisfaction of EPA, EPD, and
    environmentalists. Further complicating the situation for
    Houston County is the lack of any mass transit system to
    alleviate the problem created by the influx of so many
    additional automobiles. Unfortunately, the way the county has
    been developed would probably make a mass transit system not
    economically feasible.<br /></font></li>
  </ol><font size="3"><br /></font>

  <ol>
    <li type="1"><font size="3">The quality of life of our
    residents must be a consideration, when proposing residential
    or commercial development. As you know, there is a Green Space
    project, whereby counties set aside 20 percent of their land
    from development to provide for an enhanced quality of life for
    residents.<br /></font></li>
  </ol><font size="3"><br />
  At the present, we are in the unenviable position of having
  wall-to-wall residential and commercial development from north of
  highway 127 to the Bibb County line. There seems to be no thought
  given for anything but building more subdivisions, more strip
  malls, more acres and acres of asphalt and concrete (At least
  that will provide us with more temporary lakes and streams. Some
  close enough to be enjoyed from our living rooms.)<br />
  <br />
  Tonight, August 19<sup>th</sup>, I had the pleasure of attending
  another meeting of the Houston County Commission. The first item
  on the agenda was re-zoning application 1282. This application
  was for the 32.21 acres to be rezoned from R-1 to R-3.<br />
  <br />
  At the Commissioners&rsquo; meeting on August 5<sup>th</sup>, a
  motion was made to approve the petition. The motion died for lack
  of a second.<br />
  <br />
  After that meeting, the Chairman decided that the Board had to
  take the matter under consideration at tonight&rsquo;s meeting
  since, in his opinion, no action had been taken on the matter
  previously. That begs the question, &ldquo;Why didn&rsquo;t you
  state that at the meeting of August 5<sup>th</sup>?&rdquo; The
  answer should be self-evident. Action was taken. The Board in
  choosing not to second the motion, in effect denied the
  motion.<br />
  <br />
  At this evening&rsquo;s meeting, a motion was made and approved
  to send the petition back to the planning and zoning board. The
  commissioner making the motion did so because in his words
  &ldquo;in speaking with the other commissioners there was not
  enough votes to approve the petition.&rdquo; That brings to mind
  another question: Was there a meeting of our commissioners in
  violation of the state&rsquo;s Sunshine Laws?<br />
  <br />
  Why send the matter back to the P&amp;Z board? Is it simply in
  hopes that the opposition will go away? Maybe in time the other
  commissioners will see the error of their way and change their
  minds?<br />
  <br />
  Whatever the answer to those questions is only one of
  speculation. The current issue is whether the Chairman acted
  legally in bringing this matter back to consideration in
  violation of the Comprehensive Land Development Regulations for
  Houston County. I will seek to find a definitive answer to that
  question. If the Board acted illegally, I would expect legal
  action to be taken to correct the matter.<br />
  <br />
  David Wittenberg<br />

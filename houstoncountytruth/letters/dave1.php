<h2 class="headline">Writer thinks Planning and Zoning Committee's decisions may be based on race. </h2>

<p>04 August 2003</p>
<p>Editor:</p><p>
</p><p>
In Houston County, we seem to have an objective method for determining whether a neighborhood is worthy of high-density (R-3) subdivisions.  I really thought the whole process was purely subjective, you know &#147;a judgment call&#148;.</p><p>
Tonight I attended the monthly meeting of the Houston County Planning and Zoning (P&amp;Z) Committee.  Under consideration was a request to re-zone property at the intersection of Lake Joy Road and Sandefur Road.  No other property in the area is zoned R-3.</p><p>
Earlier this year, the county considered another request to re-zone property to R-3.  In this case, adjacent property was already zoned R-3.  No charge of spot zoning could be made in this case.  However, the rezoning of property near King&#146;s Crossing was denied.</p><p>
In the case of the request to re-zone the property at Lake Joy and Sandefur Road, the P&amp;Z Committee recommended approval for the second time.</p><p>
I asked myself, &#147;Why?&#148;  What was the objective determinant factor that would permit approval of the Lake Joy re-zoning?  Then, the answer came through loud and clear.</p><p>
As a resident pointed out at the Committee meeting, &#147;They think of us as poor white trash.&#148;  But, that&#146;s only part of the answer.  You see, in our community we have a solid mix of black and white residents.  Could it be that the objective factor used by the county to determine where high density developments are allowed, is simply based on the neighborhood&#146;s racial composition?</p><p>
</p><p>
David E. Wittenberg</p>

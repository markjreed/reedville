  <h2 class="headline">Citizen asks Houston County District
  Attorney Kelly Burke for opinion on rezoning vote legality.
  District Attorney Kelly Burke will not respond.</h2>September 17,
  2003<br />
  <br />
  Kelly Burke<br />
  District Attorney, Houston County<br />
  North 201 Perry Parkway<br />
  Perry, Ga. 31069<br />
  <br />
  <br />
  Sir:<br />
  <br />
  I am writing seeking your assistance in determining whether
  violations of existing statutes has occurred as committed by the
  Houston County Board of Commissioners.<br />
  <br />
  There are two points which I wish to bring to your attention.
  First, I believe a violation of the Comprehensive Land
  Development Regulation for Houston County may have occurred on
  August 19<sup>th</sup>. For your reference, I refer to sections
  116.5, 116.7, and 161.<br />
  <br />
  On August 5<sup>th</sup> during its regularly scheduled meeting,
  the Board of Commissioners considered two re-zoning petitions
  requested by Mr. Charles McGlamry and Mr. Howard Wooden. The
  petition is known as Rezoning Application #1282. One of the
  petitions was granted. The second, a request to rezone 32.21
  acres from R-1 to R-3 was denied by the lack of a second to the
  motion to approve made by Commissioner McMichael.<br />
  <br />
  Following that meeting, the Commission Chairman decided that the
  lack of a second to the motion did not adequately address the
  petition and he re-scheduled another vote on the issue for the
  meeting held August 19<sup>th</sup>. In his remarks prior to
  placing the matter before the Board for consideration, the
  Chairman stated that the Land Development Regulation required an
  &ldquo;up or down&rdquo; vote on the matter.<br />
  <br />
  There are several points that contradict his explanation. First,
  section 116.5 of subject regulation states in part &ldquo;The
  Governing Body shall hold a public hearing at the earliest
  possible time under the time limits spelled out above to consider
  the proposed zoning amendment, and shall <b>take action on said
  proposed zoning amendment within 45 days from the date of the
  public hearing&hellip;.&rdquo;.</b> With all respect to the
  Chairman, I contend that action on the proposed zoning amendment
  was taken by the Board when the motion to approve was made and
  died for the lack of a second to the motion. Next, the
  Chairman&rsquo;s actions provide a method to circumvent section
  116.7, which requires a six month delay prior to re-submitting a
  zoning amendment on the same property. I believe that the
  Chairman&rsquo;s actions negate equal and due process
  opportunities to all residents of Houston County.<br />
  <br />
  Additionally, at the August 19<sup>th</sup> meeting called to
  address the original question, the original motion was never
  addressed. Instead, a motion to send the application back to the
  Planning and Zoning Board was made and passed. This constitutes
  new action on the rezoning request somewhat the same as if the
  original petitioner had made a second request for review of the
  application. The actions of the Chairman and the Board certainly
  conflict with the spirit and intent of section 116.5 and
  116.7.<br />
  <br />
  Sir, my question for your review is, &ldquo;Have the actions of
  the Commission violated the Land Development Regulation such that
  a violation of Section 69-9905 of the Georgia Code Annotated may
  have occurred?&rdquo;<br />
  <br />
  The second question I wish to bring to your attention is whether
  the Board of Commissioners has violated the Georgia Open Meetings
  law.<br />
  <br />
  Commissioner Tom McMichael is quoted on page 1B of the August
  20<sup>th</sup> issue of the <i>Macon Telegraph</i>, &ldquo;I
  have talked considerably over the past week with the other
  commissioners and the consensus was it would not be approved as
  an R-3&rdquo;. His quoted statement indicates that Board members
  met in some method and in some number to discuss an issue that
  was addressed as official business at the August 5<sup>th</sup>
  meeting and which was known to be referred to the Board by
  Chairman at the August 19<sup>th</sup> meeting. At the very
  least, a polling of members of the Board has occurred in
  secretive session(s). This violates the citizens' right to
  witness the conduct of business on matters affecting their
  lives.<br />
  <br />
  It should also be noted, that even before the meeting convened,
  the Commission&rsquo;s decision to return the petition to the
  Planning and Zoning Board was known by the press and reported to
  citizens in attendance of the meeting. Clearly, business has been
  conducted in secret session.<br />
  <br />
  Sir, my question to you on this matter is, &ldquo;Has the
  Commission violated the state&rsquo;s Open Meeting
  laws?&rdquo;<br />
  <br />
  If you find that violation of law has occurred in either or both
  situations I have referred to you, I ask that you take such
  appropriate actions as are necessary to ensure that further
  violations of these types do not occur again.<br />
  <br />
  Let me say that I have no ill feeling toward any Commission
  member and certainly none against the re-zoning petitioners. My
  sole objective is to secure equal opportunity and treatment for
  all Houston County residents and to ensure that the Commission
  conducts all its functions in a lawful manner.<br />
  <br />
  <br />
  &gt; David E. Wittenberg<br />

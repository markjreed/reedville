<h2 class="headline">Houston County Knight Ridder Newspapers Refuse
To Accept Paid Distribution of 10,000 Newsletters That Are Critical of
Their Journalistic Ethics.</h2>

<p>The Macon <em>Telegraph</em>, Warner Robins Daily <em>Sun</em>, and
the Warner Robins <em>Buyers&rsquo; Guide</em> all refused to insert the
<em>&ldquo;Houston County Truth&rdquo;</em> as a paid flyer to be
distributed with their newspaper like an advertisement. I am not
surprised, just disappointed. Their refusal just proves my point
that they will not let the truth become public if they can help it.
Too bad. All of the copies will be distributed anyway and the
people will also know that the newspapers did their best to prevent
it. I am really grateful that they have done this. They have proved
my point for me.</p>


<p>Official Reasons Given For Refusal to Distribute:</p>

<ol>
<li> W. R. <em>Buyers&rsquo; Guide</em> publisher Mike Hall: His explanation
was rambling and hard to understand. I asked him if he would put it
in writing and he said no. Distribution rate: $30 / thousand.</li>

<li> W. R. Daily <em>Sun</em> General Manager John Hall: John&rsquo;s reason
was &ldquo;publisher&rsquo;s discretion&rdquo;. Distribution rate:
$37 / thousand.</li>

<li>Macon <em>Telegraph</em> publisher Jeannie Enyart: Their
official stated reason is that they choose not to insert my one
page newsletter as a paid flyer. Distribution rate: $38.50 /
thousand.</li>

